# Initial value
a = int(input("Enter a: "))
b = int(input("Enter b: "))

# Bitwise AND
result = a & b
print("Bitwise AND:", result)

# Bitwise OR
result = a | b
print("Bitwise OR:", result)

# Bitwise XOR
result = a ^ b
print("Bitwise XOR:", result)

# Bitwise NOT
result = ~a
print("Bitwise NOT:", result)

# Bitwise left shift
result = a << 2
print("Bitwise left shift:", result)

# Bitwise right shift
result = a >> 2
print("Bitwise right shift:", result)