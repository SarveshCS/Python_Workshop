# Initial value
a = 10  # 1010 in binary
b = 4   # 0100 in binary

# Bitwise AND
result = a & b
print("Bitwise AND:", result)

# Bitwise OR
result = a | b
print("Bitwise OR:", result)

# Bitwise XOR
result = a ^ b
print("Bitwise XOR:", result)

# Bitwise NOT
result = ~a
print("Bitwise NOT:", result)

# Bitwise left shift
result = a << 2
print("Bitwise left shift:", result)

# Bitwise right shift
result = a >> 2
print("Bitwise right shift:", result)