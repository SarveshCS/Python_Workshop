# Creating single-element tuples and lists
single_element_tuple = (42,)  # Note the comma
single_element_list = [42]     # No comma needed

# Displaying the types and values
print("Single-element tuple:", single_element_tuple, "Type:", type(single_element_tuple))
print("Single-element list:", single_element_list, "Type:", type(single_element_list))

# Accessing elements
print("Accessing tuple element:", single_element_tuple[0])
print("Accessing list element:", single_element_list[0])