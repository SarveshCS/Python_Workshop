# Copying the set
original_set = {1, 2, 3}
copied_set = original_set.copy()
print("Original set:", original_set)
print("Copied set:", copied_set)

# Length of a set
length = len(original_set)
print("Length of the original set:", length)

# Checking membership
is_in_set = 1 in original_set
print("Is 1 in the original set?", is_in_set)

is_not_in_set = 6 not in original_set
print("Is 6 not in the original set?", is_not_in_set)